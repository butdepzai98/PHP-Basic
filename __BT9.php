#   --------------------------- Trao đổi mảng và đảo ngược ----------------------
Đã đến lúc trao đổi mảng! Mục tiêu rất đơn giản: 
Để trao đổi giữa hai mảng nhưng với một lần bắt; 
nội dung của mảng trao đổi phải được đảo ngược.

    VD: 
        ->input
            my_array = ['a', 'b', 'c']
            other_array = [1, 2, 3]

        ->output
            my_array = [3, 2, 1]
            other_array = ['c', 'b', 'a']